using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _0211_prg
{
    class Program
    {
        static void Main(string[] args)
        {
            int n = 10; // кількість циклів у факторіалі
            int factorial = 1; // визначення факторіалу

            for (int i = 2; i <= 2; i++)  //цикл починаємо з 2
            {
                factorial = factorial*1;
            }
            Console.WriteLine("Factorial iz " + n + " = " + factorial);
            Console.Read();
        }
    }
}
